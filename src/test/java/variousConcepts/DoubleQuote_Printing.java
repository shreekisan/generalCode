package variousConcepts;

public class DoubleQuote_Printing {

	public static void main(String[] args) {
		/*
		 "Hello"
		 /"Hello"/
		 /'Hello'/
		 '/'Hello'/'
		 "/'Hello'/"	 
		  I love "java" and "programming" and "movies"
		  'I love "java" and "programming" and "movies"'
		*/
		System.out.println("\"Hello\""); //In beginning before slash exclude, at end after slash last is exclude
		System.out.println("/\"Hello\"/");
		System.out.println("/\'Hello\'/");
		System.out.println("\"/'Hello\'/\"");
		System.out.println("\'/'Hello\'/\'");
		
		System.out.println("I love \"java\" and \"programming\" and \"movies\"");
		System.out.println("\'I love \"java\" and \"programming\" and \"movies\"'");
		

	}

}
